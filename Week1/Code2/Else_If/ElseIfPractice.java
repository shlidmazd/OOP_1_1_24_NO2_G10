package Week1.Code2.Else_If;

public class ElseIfPractice {
    public static void main(String[] args) {
        int time = 19;
        if (time < 10) {
            System.out.println("Good morning!");
        } else if (time < 18) {
            System.out.println("Good day!");
        } else {
            System.out.println("Good evening!");
        }
    }
}
